package gameEngine.world;

/**
 * stores the constants that specify the depth each facet of the game
 * world is to be drawn at
 * @author Jack
 *
 */
public final class DepthConstants
{
	/**
	 * the depth the background color is drawn at
	 */
	public static final double background = -1;
	/**
	 * the depth terrain is drawn at
	 */
	public static final double terrain = -.8;
}
